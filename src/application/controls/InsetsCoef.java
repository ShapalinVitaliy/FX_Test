package application.controls;

public class InsetsCoef {

		double UpCoef = 0;
		double DownCoef = 0;
		double LeftCoef = 0;
		double RightCoef = 0;
		
		public InsetsCoef(double UpCoef, double DownCoef, double LeftCoef, double RightCoef) {
			this.UpCoef = UpCoef;
			this.DownCoef = DownCoef;
			this.LeftCoef = LeftCoef;
			this.RightCoef = RightCoef;
		
		}
}
